<template lang="">
  <div class="tab-control">
    <div v-for="(item, index) in titles"
          class="tab-control-item"
          :class=" {active: index === currentIndex}" 
          @click="itemClick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>
<script>
  export default {
    name: 'TabControl',
    props: {
      titles: {
        type: Array,
        default() {
          return []
        }
      }
    },
    data() {
      return {
        currentIndex: 0
      }
    },
    methods: {
      itemClick(index) {
        this.currentIndex = index
        this.$emit('tabClick', index)
      }
    }
  }
</script>
<style scoped>
  .tab-control {
    background-color: #fff;
    height: 40px;
    line-height: 40px;
    text-align: center;
    display: flex;
    position: relative;
    z-index: 9;
  }

  .tab-control-item {
    flex: 1;
  }

  .tab-control-item span {
    padding: 5px;
  }

  .active span {
    color: var(--color-high-text);
    border-bottom: 2px solid var(--color-tint);
  }
</style>